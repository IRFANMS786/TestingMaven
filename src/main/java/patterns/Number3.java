package patterns;

import java.util.Scanner;

public class Number3 {
	public static void main(String[] args) {
		int i,j;
		try (Scanner s1 = new Scanner(System.in)) {
			System.out.println("Enter the Number to Build the Pattern");
			int n = s1.nextInt();
			for ( i = 1; i <= n; i++) {
				System.out.print(" ");
			}
			
			for ( j = 1; i <= i; i++) {
				System.out.println(i);
			}
			System.out.println();
			
		}
	}

}
